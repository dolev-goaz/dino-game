;----------------------------------------------------
;RandNum- Generating a number
;----------------------------------------------------
;Input:
;            AX
;Output:
;            AX
;Registers:
;            AX
;----------------------------------------------------
PROC RandNum
	cmp al, 2
	je Rand2Numbers
	cmp al, 3
	je Rand3Numbers
	jmp EndRand
	
	
Rand2Numbers:
	xor ax, ax
	in al, 40h
	shr al, 6
	;if al=0, output=0
	;if al=1, output=1
	jmp EndRand
	
Rand3Numbers:
	xor ax, ax
	in al, 40h
	shr al, 6
	cmp al, 0
	je Rand3Numbers ;output only 1,2,3
	
EndRand:
	ret
ENDP RandNum


;----------------------------------------------------------
; PutPixel Drawing point (direct memory access)
;----------------------------------------------------------
; Input:
; 	AX = x, BX = y, CL = color 
; Output:
; 	The point
; Registers
;	AL, BX, CL, DX, DI
;----------------------------------------------------------
PROC PutPixel
; in order to get one line lower you to add 320 to the pointer everytime you go one line lower
; you add the x to get to the starting position of the line
		mov dx, bx ; bx = y = dx
		shl bx, 6  ; bx = bx^8 = y^8
		shl dx, 8  ; dx = dx^8 = y^6
		
		mov di, ax ; di = ax = x
		add di, bx ; di = y^6 + x
		add di, dx ; di = y^8 + y^6 + x
;		di = y*320+x = y*(2^8) + y*(2^6) + x = (y<<8) + (y<<6) + x
		mov al, cl ; cl = color
		stosb		; mov es:[di], al. inc di (draws the pixel)
		ret
ENDP PutPixel



